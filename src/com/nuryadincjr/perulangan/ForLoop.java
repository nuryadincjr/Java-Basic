package com.nuryadincjr.perulangan;

public class ForLoop {
    /*
    Statemen For: Perulangan dengan penjabaran nilai eksplisit.
    for (initialization; condition; step_value) {
        // statement aksi
    }
    */

    public static void main(String[] args) {
        for(int i = 0; i < 10; i++) {
            System.out.println(i);
        }
    }
}
