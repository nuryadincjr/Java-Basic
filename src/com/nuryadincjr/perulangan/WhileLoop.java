package com.nuryadincjr.perulangan;

public class WhileLoop {
    /*
    Statemen While: Pengulangan selama ekspresi kondisi boolean bernilai true.
    while(condition) {
        // statement aksi dan increment/decrement
    } 
    */

    public static void main(String[] args) {
        int x = 0;
        while (x < 10) {
            System.out.println(x);
            x++;
        }
        
    }
}
