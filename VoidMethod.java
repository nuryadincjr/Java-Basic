public class VoidMethod {
    /*
    Void Method: method bertipe void dan tidak mengembalikan nilai apapun.
    void namaMethod() {
        // stetmen aksi
    }
    */

    static void getURL() {
        System.out.println("https://github.com/nuryadin.cjr");
    }

    public static void main(String[] args) {
        getURL();
    }
}
