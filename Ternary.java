public class Ternary {
/*
Operator Tenary: Memperingkas If statement.
<type_data> namaVariable = <expression> ? <statement_true> : <statement_false>;
*/

    public static void main(String[] args) {
        int nilai = 80;
        int skbm = 75;

        System.out.println( (nilai > skbm) ? "Nilai di atas rata-rata" : "Nilai dibawah rata-rata");
    }
}
